"""
SAT_BASIL — ML hyperparameter tuning (v2: SNV + cluster augmentation)
======================================================================
- Sensor × Scope × Data × Norm × Model
  - sensor: vn, sw
  - scope : all_leaf, c0, c1, c2
  - data  : raw, ae, gan
  - norm  : none, snv
  - model : RandomForest, SVM, XGBoost, KNN
- Total combos: 2 × 4 × 3 × 2 × 4 = 192
- Tuning: Optuna 200 trials × 5-fold CV, TPE + MedianPruner
- Pipeline: load → (SNV optional) → StandardScaler → model
- Storage : Optuna SQLite + best_params.json + master_results.csv

Notes:
- Per-cluster (c0/c1/c2) splits are produced by
  preprocessing/make_per_cluster_csvs.py and written to data/per_cluster_split/.
  scope=all_leaf can be reproduced directly from data/train_test_split/.
- Augmentation CSVs live in data/augmentation/ (ae|gan)_{swir,vnir}_n300.csv.
  Pass --aug_root only if you placed augmented files elsewhere.
"""
